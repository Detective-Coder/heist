namespace heist
{
  public class TeamMember
  {
    /* Create a way to store a single team member. A team member will have a name, skill level, and courage factor. The skill level will be a positive integer, and the courage factor will be a decimal between 0.0 and 2.0.
    */
    public string Name {get; set;}
    public int SkillLevel {get; set;}
    public float CourageFactor {get; set;}
  }
}